(** each Rpc.t variant represented at least once,
    the other variants represented using a power of 2 value
    close to the average of vm+pool *)
let dummy =
  let rpc_variants = 10 in
  (* fixed *)
  let nested_dict_length = 16 in
  let dict_key_length = 32 in
  let string_length = 512 in
  let tag_ratio = 64 in
  let quote_ratio = 8 in
  let dict_keys_count = rpc_variants + nested_dict_length in
  ( "dummy"
  , Rpc_stats.of_stats
      ~int:1
      ~int32:1
      ~bool:8
      ~float:1
      ~string:
        ( [| nested_dict_length, nested_dict_length; string_length, 1 |]
        , [ '<', [| string_length / tag_ratio, 1 |]
          ; '>', [| string_length / tag_ratio, 1 |]
          ; '"', [| 2 * string_length / quote_ratio, 1 |]
          ] )
      ~datetime:1
      ~enum_length:[| 1, 1 |]
      ~dict_keys:[| dict_key_length * dict_keys_count, dict_keys_count |]
      ~dict_length:[| nested_dict_length, 1 |]
      ~base64:[| 1, 1 |]
      ~null:1
    |> Rpc_stats.rpc_of_stats )


let bench_protocol rpc name of_string to_string =
  let serialized = to_string rpc in
  let open Bechamel in
  let encode () = to_string rpc
  and decode () = of_string serialized in
  Test.make ~name Staged.(stage encode), Test.make ~name Staged.(stage decode)


let make_tests (name, rpc) =
  let xml1, xml2 = bench_protocol rpc "xmlrpc" Xmlrpc.of_string Xmlrpc.to_string
  and json1, json2 = bench_protocol rpc "jsonrpc" Jsonrpc.of_string Jsonrpc.to_string in
  let rpc_str = rpc |> Xmlrpc.to_string in
  let rpc_id = rpc_str |> Digest.string |> Digest.to_hex in
  (* benchmarks are only comparable when run on same input *)
  Format.eprintf "%s: %s (%d)@." name rpc_id (String.length rpc_str);
  Bechamel.Test.make_grouped
    ~name
    [ Bechamel.Test.make_grouped ~name:"to_string" [ xml1; json1 ]
    ; Bechamel.Test.make_grouped ~name:"of_string" [ xml2; json2 ]
    ]


let tests = [ dummy; pool; vm ] |> List.map make_tests
let () = Bechamel_simple_cli.cli tests
